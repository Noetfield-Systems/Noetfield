#!/usr/bin/env node
/** Generate Cloudflare Pages Functions wrappers from api/ Vercel handlers. */

import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const API_ROOT = path.join(ROOT, "api");
const FUNCTIONS_ROOT = path.join(ROOT, "functions");

function walkApiRoutes(dir, base = "") {
  const routes = [];
  for (const name of fs.readdirSync(dir)) {
    if (name === "_lib") continue;
    const full = path.join(dir, name);
    const rel = base ? `${base}/${name}` : name;
    if (fs.statSync(full).isDirectory()) {
      routes.push(...walkApiRoutes(full, rel));
      continue;
    }
    if (!name.endsWith(".js")) continue;
    routes.push(rel.replace(/\\/g, "/"));
  }
  return routes;
}

function functionOutPath(apiRel) {
  const parts = apiRel.split("/");
  if (parts[parts.length - 1] === "index.js") {
    parts.pop();
    const suffix = parts.length ? parts.join("/") : "index";
    return path.join(FUNCTIONS_ROOT, "api", `${suffix}.js`);
  }
  const file = parts.pop().replace(/\.js$/, "");
  return path.join(FUNCTIONS_ROOT, "api", ...parts, `${file}.js`);
}

function handlerImportPath(apiRel, functionFile) {
  const posix = functionFile.replace(/\\/g, "/");
  const afterApi = posix.split("/api/")[1] || "";
  const dirDepth = afterApi.includes("/") ? afterApi.split("/").length - 1 : 0;
  const prefix = `${"../".repeat(dirDepth + 1)}_lib/handlers/`;
  return `${prefix}${apiRel}`;
}

function adapterImportPath(functionFile) {
  const rel = path.relative(path.dirname(functionFile), path.join(FUNCTIONS_ROOT, "_lib", "vercel-adapter.js"));
  return rel.replace(/\\/g, "/");
}

function renderWrapper(apiRel, functionFile) {
  const handlerImport = handlerImportPath(apiRel, functionFile);
  const adapterPath = adapterImportPath(functionFile);
  return `/** Generated by scripts/generate-pages-functions.mjs — do not edit. */
import { runVercelHandler } from "${adapterPath}";
import * as handlerModule from "${handlerImport}";

const handler = handlerModule.default || handlerModule;
export const onRequest = (context) => runVercelHandler(handler, context);
`;
}

function main() {
  const routes = walkApiRoutes(API_ROOT);
  const generated = [];

  for (const apiRel of routes) {
    const out = functionOutPath(apiRel);
    fs.mkdirSync(path.dirname(out), { recursive: true });
    fs.writeFileSync(out, renderWrapper(apiRel, out), "utf8");
    generated.push(path.relative(ROOT, out));
  }

  console.log(`generate-pages-functions: wrote ${generated.length} wrappers`);
  for (const file of generated.sort()) {
    console.log(`  ${file}`);
  }
}

main();
